CREATE TABLE AREA(
AREA_CODE  	       VARCHAR(2),
AREA_ALERTLEVEL        INTEGER,
AREA_NAME                     VARCHAR(8),
PRIMARY KEY (AREA_CODE));

CREATE TABLE HOSPITAL(
HOSPI_CODE		VARCHAR(10) NOT NULL,
AREA_CODE			VARCHAR(2) NOT NULL,
HOSPI_WARD_COUNT	VARCHAR(6),
HOSPI_NAME		VARCHAR(25),
HOSPI_PHONE		VARCHAR(15),
PRIMARY KEY (HOSPI_CODE),
FOREIGN KEY (AREA_CODE) REFERENCES AREA ON UPDATE CASCADE ON DELETE CASCADE);

CREATE TABLE VACCINE(
VAC_CODE		VARCHAR(15) NOT NULL,
VAC_COUNTRY	VARCHAR(15) NOT NULL,
VAC_QUAN		DECIMAL(12,2),
VAC_INDATE		DATE,
PRIMARY KEY (VAC_CODE));

CREATE TABLE CITIZEN (
CIT_ID   		VARCHAR(15) NOT NULL,
CIT_NAME 		VARCHAR(30) NOT NULL,
CIT_BIRTHDATE	DATE,
CIT_PHONE		VARCHAR(20),
CIT_FAMI	            VARCHAR(10),
AREA_CODE  	VARCHAR(2),
PRIMARY KEY (CIT_ID),
FOREIGN KEY (AREA_CODE) REFERENCES AREA ON UPDATE CASCADE ON DELETE CASCADE);


CREATE TABLE MEDICAL_PERSONAL(
MEDI_CODE		VARCHAR(10) NOT NULL,
CIT_ID			VARCHAR(15) NOT NULL,
HOSPI_CODE	VARCHAR(10) NOT NULL,
MEDI_POSITION	VARCHAR(10),
PRIMARY KEY (MEDI_CODE),
FOREIGN KEY (CIT_ID) REFERENCES CITIZEN ON UPDATE NO ACTION ,
FOREIGN KEY (HOSPI_CODE) REFERENCES HOSPITAL ON UPDATE CASCADE );


CREATE TABLE VACCINATION(
CIT_ID			VARCHAR(15) NOT NULL,
VAC_CODE		VARCHAR(15) NOT NULL,
HOSPI_CODE	VARCHAR(10) NOT NULL,
VACTION_DATE	DATE,
PRIMARY KEY (CIT_ID),
FOREIGN KEY (CIT_ID) REFERENCES CITIZEN ON UPDATE NO ACTION ON DELETE NO ACTION,
FOREIGN KEY (VAC_CODE) REFERENCES VACCINE ON UPDATE NO ACTION ON DELETE NO ACTION,
FOREIGN KEY (HOSPI_CODE) REFERENCES HOSPITAL ON UPDATE CASCADE ON DELETE CASCADE);


CREATE TABLE CONFIRMED(
CON_CODE		VARCHAR(15),
CIT_ID			VARCHAR(15) NOT NULL,
AREA_CODE		VARCHAR(2),
CON_CONDATE	DATE,
CON_REMARK      	VARCHAR(20),
PRIMARY KEY (CON_CODE),
FOREIGN KEY (CIT_ID) REFERENCES CITIZEN ON UPDATE NO ACTION ON DELETE NO ACTION,
FOREIGN KEY (AREA_CODE) REFERENCES AREA ON UPDATE CASCADE ON DELETE CASCADE);



CREATE TABLE SCREENED_CITIZEN(
CIT_ID			VARCHAR(15) NOT NULL,
AREA_CODE		VARCHAR(2),
CON_CODE		VARCHAR(15),
SCR_TIME		DATE,
SCR_SCRNAME	VARCHAR(15) NOT NULL,
PRIMARY KEY (CIT_ID),
FOREIGN KEY (AREA_CODE) REFERENCES AREA ON UPDATE NO ACTION ON DELETE NO ACTION,
FOREIGN KEY (CON_CODE) REFERENCES CONFIRMED ON UPDATE CASCADE ON DELETE CASCADE);



CREATE TABLE FOOTPRINT(
FOO_CODE		VARCHAR(15) NOT NULL,
CON_CODE		VARCHAR(15) NOT NULL,
PRIMARY KEY (FOO_CODE),
FOREIGN KEY (CON_CODE) REFERENCES CONFIRMED ON UPDATE CASCADE ON DELETE CASCADE);


CREATE TABLE FOOTPRINT_LINE(
FOO_CODE		VARCHAR(15) NOT NULL,
AREA_CODE		VARCHAR(2),
LINE_LOCATION 	VARCHAR(50) NOT NULL,
LINE_TIME		DATETIME,
PRIMARY KEY (FOO_CODE,AREA_CODE),
FOREIGN KEY (FOO_CODE) REFERENCES FOOTPRINT ON UPDATE NO ACTION ON DELETE NO ACTION,
FOREIGN KEY (AREA_CODE) REFERENCES AREA ON UPDATE CASCADE ON DELETE CASCADE);


CREATE TABLE ANTI_HOTEL(
HOTEL_CODE		VARCHAR(15) NOT NULL,
AREA_CODE  		VARCHAR(2) NOT NULL,
HOTEL_NAME		VARCHAR(50) NOT NULL,
HOTEL_ADDRESS		VARCHAR(50) NOT NULL,
HOTEL_ROOMQUANTITY	DECIMAL(12,2),
PRIMARY KEY (HOTEL_CODE),
FOREIGN KEY (AREA_CODE) REFERENCES AREA ON UPDATE CASCADE ON DELETE CASCADE);


