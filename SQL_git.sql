/*匡列家屬*/
SELECT DISTINCT C1.CIT_ID,C1.CIT_NAME,C1.CIT_PHONE, C1.CIT_FAMI
FROM CITIZEN C1 JOIN (SELECT CITIZEN.CIT_FAMI 
						FROM CITIZEN 
						WHERE CITIZEN.CIT_ID IN (SELECT CONFIRMED.CIT_ID FROM CONFIRMED))C2 ON C1.CIT_FAMI=C2.CIT_FAMI
WHERE CIT_ID NOT IN (SELECT CIT_ID FROM CONFIRMED)
ORDER BY CIT_FAMI;


/*疫苗沒用與有效的比率*/
SELECT DISTINCT (SELECT COUNT(*) FROM CONFIRMED,VACCINATION WHERE CONFIRMED.CIT_ID=VACCINATION.CIT_ID)*1.0/(SELECT COUNT(*) FROM VACCINATION)AS USELESS,
		 1-(SELECT COUNT(*) FROM CONFIRMED,VACCINATION WHERE CONFIRMED.CIT_ID=VACCINATION.CIT_ID)*1.0/(SELECT COUNT(*) FROM VACCINATION) AS EFFICACY
FROM CONFIRMED;


/*廠牌疫苗詳細保護力*/
SELECT DISTINCT 
1-(SELECT COUNT(VACCINATION.CIT_ID)			/*接種'MED-TAI01'可是又確診的人數*/
FROM VACCINATION,CONFIRMED
WHERE VACCINATION.CIT_ID=CONFIRMED.CIT_ID
AND VACCINATION.VAC_CODE LIKE 'MED-TAI01')*1.0
/											/*相除*/
(SELECT COUNT(VACCINATION.VAC_CODE)			/*接種'MED-TAI01'人數*/
FROM VACCINATION
WHERE VAC_CODE LIKE 'MED-TAI01')*1.0
AS VACPERCENT
FROM VACCINATION


/*足跡傳播鏈*/
SELECT DISTINCT FOOTPRINT.CON_CODE,FOOTPRINT_LINE.LINE_LOCATION
FROM FOOTPRINT,FOOTPRINT_LINE
WHERE FOOTPRINT.FOO_CODE=FOOTPRINT_LINE.FOO_CODE AND FOOTPRINT_LINE.LINE_LOCATION IN (SELECT FOOTPRINT_LINE.LINE_LOCATION 
																						FROM FOOTPRINT,FOOTPRINT_LINE
																						WHERE FOOTPRINT.FOO_CODE=FOOTPRINT_LINE.FOO_CODE
																						GROUP BY LINE_LOCATION
																						HAVING COUNT(FOOTPRINT_LINE.LINE_LOCATION)>1)
ORDER BY FOOTPRINT_LINE.LINE_LOCATION


/*快篩確診率*/
